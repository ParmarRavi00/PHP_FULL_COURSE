Loops: to repeat the code 

=> intialization (assignment) (x=1) 
=> condition (x<=3)
=> inc/dec  (x++)

for,while : entry control loops 
do while : exit control loop


1. for 

Syntax: 
for(intialization;condition;inc/dec)
{
    ..code;
}

2. while 

intialization;

while(condition)
{
    ...code;
    inc/dec;
}


3. do while 


intialization;

do
{
    ...code;
    inc/dec;
}while(condition);

